# Today we are going to create a Dictionary
Deva = {'Banana' : 'kella' , 'Pomegranate' : 'Anaar' , 'Guava' : 'Amrud' , 'Watermelon' : 'Tarbuj' , 'Grapes' : 'Angur'}
print(Deva)
# Using Its Functions
print(Deva.get('Banana'))
print(Deva.get('Watermelon'))

print(Deva.keys())
print(Deva.values())

print(Deva.copy())

Deva.update({'Apple' : 'Seb'})
Deva['Pineapple'] = 'Ananaas'

del Deva['Apple']
print(Deva)

print(Deva.items())
print(Deva.clear())

